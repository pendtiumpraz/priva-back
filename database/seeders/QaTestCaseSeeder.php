    public function run(): void
    {
        // Hapus existing built-in supaya re-seed bersih.
        // Custom cases (is_built_in=false) tetap aman.
        QaTestCase::where('is_built_in', true)->forceDelete();

        $catalog = $this->catalog();
        $sortBase = 0;

        foreach ($catalog as $module => $items) {
            $sortBase += 100;
            foreach ($items as $i => $row) {
                QaTestCase::updateOrCreate(
                    [
                        'module' => $module,
                        'feature' => $row[0],
                        'interaction' => $row[1],
                    ],
                    [
                        'title' => $row[3],
                        'description' => $row[4] ?? null,
                        'applicable_roles' => $row[2],
                        'is_built_in' => true,
                        'is_active' => true,
                        'sort_order' => $sortBase + $i,
                    ]
                );
            }
        }
    }

